#include <unistd.h>

int	main(int a, char *str[])
{
	int		i;
	char	*c;

	c = str[0];
	i = 0;
	while (c[i] && a)
	{
		i++;
	}
	write(1, c, i);
	write(1, "\n", 1);
	return (0);
}
